import { BasePanelMenuItem } from "primeng/primeng";

export class workWip {
  'lottype' = ' ';
  'fgcode' = ' ';
  'workorder' = ' ';
  'wostate' = ' ';
  'bangding' = ' ';
  'runtime' = ' ';
  'planqty' = ' ';
  'assigntowo' = ' ';
  'notstart' = ' ';
  'wip' = ' ';
  'shipped' = ' ';
  'fgmsnoreceipt' = ' ';
  'scrap' = ' ';
  'reworkqty' = ' ';

  setProperties(name:string,value:string){
    if(name == 'lottype'){
      this['lottype'] = value;
    }else if(name == 'fgcode'){
      this['fgcode'] = value;
    }else if(name == 'workorder'){
      this['workorder'] = value;
    }else if(name == 'wostate'){
      this['wostate'] = value;
    }else if(name == 'bangding'){
      if(value == null){
        value = '';
      }
      this['bangding'] = value;
    }else if(name == 'runtime'){
      this['runtime'] = value;
    }else if(name == 'planqty'){
      this['planqty'] = value;
    }else if(name == 'assigntowo'){
      this['assigntowo'] = value;
    }else if(name == 'notstart'){
      this['notstart'] = value;
    }else if(name == 'wip'){
      this['wip'] = value;
    }else if(name == 'shipped'){
      this['shipped'] = value;
    }else if(name == 'fgmsnoreceipt'){
      this['fgmsnoreceipt'] = value;
    }else if(name == 'scrap'){
      this['scrap'] = value;
    }else if(name == 'reworkqty'){
      this['reworkqty'] = value;
    }
  }
}