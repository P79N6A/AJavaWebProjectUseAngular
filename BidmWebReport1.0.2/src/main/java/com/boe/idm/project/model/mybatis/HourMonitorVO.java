package com.boe.idm.project.model.mybatis;

import org.apache.ibatis.type.Alias;

@Alias("HourMonitorVO")
public class HourMonitorVO {
	private String hour;
	private String item;
	private Double qty;
	private Double planqty;
	private Double balqty;
	private Double actplan;

	public String getHour() {
		return hour;
	}

	public void setHour(String hour) {
		this.hour = hour;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public Double getQty() {
		return qty;
	}

	public void setQty(Double qty) {
		this.qty = qty;
	}

	public Double getPlanqty() {
		return planqty;
	}

	public void setPlanqty(Double planqty) {
		this.planqty = planqty;
	}

	public Double getBalqty() {
		return balqty;
	}

	public void setBalqty(Double balqty) {
		this.balqty = balqty;
	}

	public Double getActplan() {
		return actplan;
	}

	public void setActplan(Double actplan) {
		this.actplan = actplan;
	}

}
